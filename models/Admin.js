const mongoose = require('mongoose');

const adminSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    // Add any additional fields or permissions specific to admins
});

const Admin = mongoose.model('Admin', adminSchema);

module.exports = Admin;
